    /*This method is called when the user clicks on the close menu item under 
     *the file menu. This method provides multiple choices for the user to decide
     * on whether to exit the system, minimize the screen or cancel the process.
     */
    private void mnItemCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnItemCloseActionPerformed
        String[] options = {"Exit", "Minimize", "Cancel"};
        
        JOptionPane ask = new JOptionPane();
        int response = ask.showOptionDialog(null, "Are you sure you want to close the client?", "Close", 0,JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        
        switch (response) {
            case 0:
                System.exit(0);
                break;
            case 1:
                this.setState(this.ICONIFIED);
                break;
            case 2:
                ask.setEnabled(false);
        }
    }//GEN-LAST:event_mnItemCloseActionPerformed

